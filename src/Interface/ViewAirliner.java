    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        int selectedrow=airTable.getSelectedRow();
        if(selectedrow>=0)
        {Airliner air=(Airliner)airTable.getValueAt(selectedrow, 0);
        TA.deleteairline(air);
        JOptionPane.showMessageDialog(null, "Entry successfully deleted");
        Populate();
        }
        else{
            JOptionPane.showMessageDialog(null, "please select an entry");
        }
    }//GEN-LAST:event_deleteActionPerformed


