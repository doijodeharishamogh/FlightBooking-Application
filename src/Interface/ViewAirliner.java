    private void addflightButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addflightButtonActionPerformed
    int selectedrow=airTable.getSelectedRow();
    if(selectedrow>=0)
    {
    Airliner airliner=(Airliner)airTable.getValueAt(selectedrow, 0);
    AddFlight addflightpanel=new AddFlight(rightPanel,TA,airliner);
    rightPanel.add("AddflightPanel", addflightpanel);
    CardLayout layout=(CardLayout)rightPanel.getLayout();
    layout.next(rightPanel);
    }
    else
    {
        JOptionPane.showMessageDialog(null, "Please select an airliner");
    }
    
    }//GEN-LAST:event_addflightButtonActionPerformed

